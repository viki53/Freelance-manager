    {
        return !empty($this->user()->default_company);
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            // 'company_id' => 'exists:App\Models\Company,id'
        ];
    }
}
